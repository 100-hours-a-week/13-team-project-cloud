#!/usr/bin/env bash
# =============================================================================
# AMI 생성 전 정리 스크립트
# =============================================================================
# ※ 주의: 이 스크립트 실행 후 SSH 재접속 불가
# ※ 실행 즉시 AWS 콘솔 또는 로컬에서 AMI를 생성해야 함
#
# 이 스크립트가 하는 일:
#   - SSH 호스트 키 제거 → 새 인스턴스마다 고유 키 재생성 (MITM 방지)
#   - cloud-init 상태 초기화 → 새 인스턴스에서 SSH 키 주입·네트워크 초기화 재실행
#   - machine-id 초기화 → 인스턴스별 고유 식별자 보장 (DHCP, 저널 로그 구분)
#   - 로그·임시 파일 정리 → AMI 용량 절감
#
# 참조:
#   [4] https://docs.aws.amazon.com/AWSEC2/latest/UserGuide/creating-an-ami-ebs.html
#
# 사용법:
#   sudo bash cleanup-before-ami.sh
#   # 이후 로컬에서:
#   aws ec2 create-image --instance-id <ID> --name "k8s-..." --description "..."
# =============================================================================

set -euo pipefail

if [ "$(id -u)" -ne 0 ]; then
  echo "ERROR: root 권한으로 실행하세요 (sudo bash $0)"
  exit 1
fi

echo "============================================="
echo " AMI 생성 전 정리"
echo " ※ 실행 후 SSH 재접속 불가"
echo "============================================="
echo ""
read -r -p "계속하시겠습니까? (y/N): " confirm
if [[ "$confirm" != "y" && "$confirm" != "Y" ]]; then
  echo "취소됨."
  exit 0
fi

echo ""

# -----------------------------------------------------------------------------
# 1. 로그 정리
# -----------------------------------------------------------------------------
echo "[1/5] 로그 정리..."
journalctl --vacuum-time=1s 2>/dev/null || true
find /var/log -type f -name "*.log" -exec truncate -s 0 {} \;
find /var/log -type f -name "*.gz" -delete 2>/dev/null || true

# -----------------------------------------------------------------------------
# 2. 임시 파일 정리
# -----------------------------------------------------------------------------
echo "[2/5] 임시 파일 정리..."
rm -rf /tmp/* /var/tmp/*

# -----------------------------------------------------------------------------
# 3. machine-id 초기화
#    비워두면 첫 부팅 시 systemd-machine-id-setup이 새 ID 생성
# -----------------------------------------------------------------------------
echo "[3/5] machine-id 초기화..."
truncate -s 0 /etc/machine-id

# -----------------------------------------------------------------------------
# 4. cloud-init 상태 초기화
#    "이미 실행 완료" 기록을 지워서 새 인스턴스에서 초기화 재실행
# -----------------------------------------------------------------------------
echo "[4/5] cloud-init 초기화..."
cloud-init clean --logs 2>/dev/null || true

# -----------------------------------------------------------------------------
# 5. SSH 호스트 키 제거 (이 시점부터 새 SSH 연결 불가)
#    cloud-init이 첫 부팅 시 재생성
# -----------------------------------------------------------------------------
echo "[5/5] SSH 호스트 키 제거..."
rm -f /etc/ssh/ssh_host_*

echo ""
echo "============================================="
echo " 정리 완료 — SSH 재접속 불가"
echo "============================================="
echo ""
echo " 로컬에서 AMI 생성:"
echo "   aws ec2 create-image --instance-id <INSTANCE_ID> \\"
echo "     --name \"k8s-<VERSION>-\$(date +%Y%m%d)\" \\"
echo "     --description \"Kubernetes base AMI (kubeadm+containerd)\""
echo ""
echo " ※ --no-reboot 미사용 권장 (재부팅 후 파일시스템 정합성 보장)"
echo "============================================="
